// ===== เลขชะตา (Life Path Number) =====
export function calcLifePath(day: number, month: number, year: number): number {
  const reduceDigits = (n: number): number => {
    if (n <= 9) return n;
    const sum = String(n).split('').reduce((acc, d) => acc + Number(d), 0);
    return reduceDigits(sum);
  };
  const d = reduceDigits(day);
  const m = reduceDigits(month);
  const y = reduceDigits(year);
  return reduceDigits(d + m + y);
}

