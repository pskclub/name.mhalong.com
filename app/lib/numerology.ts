// Unicode map for Thai characters
export function getCharValue(ch: string) {
  const code = ch.charCodeAt(0);

  // Check for English letters first (case-insensitive)
  const upperCh = ch.toUpperCase();
  const englishMap: Record<string, number> = {
    // 1: A, I, J, Q, Y
    'A': 1, 'I': 1, 'J': 1, 'Q': 1, 'Y': 1,
    // 2: B, K, R
    'B': 2, 'K': 2, 'R': 2,
    // 3: C, G, L, S
    'C': 3, 'G': 3, 'L': 3, 'S': 3,
    // 4: D, M, T
    'D': 4, 'M': 4, 'T': 4,
    // 5: E, H, N, X
    'E': 5, 'H': 5, 'N': 5, 'X': 5,
    // 6: U, V, W
    'U': 6, 'V': 6, 'W': 6,
    // 7: O, Z
    'O': 7, 'Z': 7,
    // 8: F, P
    'F': 8, 'P': 8,
  };

  if (englishMap[upperCh]) {
    return englishMap[upperCh];
  }

  // Revised accurate mapping from the table image
  // 1: ก ด ท ถ ภ ฤ / สระ อา อุ อำ / ไม้เอก
  // 2: ข ช บ ป ง / สระ เอ แอ อู / ไม้โท
  // 3: ฆ ฯ ฌ ต ฃ / ไม้จัตวา (้ is mai tho, ๊ is mai tri, ๋ is mai chattawa)
  // 4: ค ธ ร ญ ษ / สระ โอ อะ อิ, ไม้หันอากาศ
  // 5: ฉ ณ ฒ น ม ห ฮ ฤ พ / สระอี
  // 6: จ ล ว อ / สระใ
  // 7: ศ ส ซ / สระ อี อือ / ไม้ตรี
  // 8: ย พ ฟ ผ ฝ / สระ ไม้ไต่คู้
  // 9: ญ ฐ / สระไ / การันต์

  const fullMap = {
    // พยัญชนะ group 1
    0x0E01:1, // ก
    0x0E14:1, // ด
    0x0E17:1, // ท
    0x0E16:1, // ถ
    0x0E20:1, // ภ
    0x0E24:1, // ฤ
    // group 2
    0x0E02:2, // ข
    0x0E0A:2, // ช
    0x0E1A:2, // บ
    0x0E1B:2, // ป
    0x0E07:2, // ง
    // group 3
    0x0E06:3, // ฆ
    0x0E2F:3, // ๆ? no ฯ is 0x0E2F
    0x0E0C:3, // ฌ
    0x0E15:3, // ต
    0x0E03:3, // ฃ
    0x0E0E:3, // ฎ
    0x0E0F:3, // ฏ
    0x0E3A:3, // ็
    // group 4
    0x0E04:4, // ค
    0x0E18:4, // ธ
    0x0E23:4, // ร
    0x0E0D:4, // ญ
    0x0E29:4, // ษ
    0x0E05:4, // ฅ
    // group 5
    0x0E09:5, // ฉ
    0x0E13:5, // ณ
    0x0E12:5, // ฒ
    0x0E19:5, // น
    0x0E21:5, // ม
    0x0E2B:5, // ห
    0x0E2E:5, // ฮ
    0x0E1E:5, // พ
    0x0E11:5, // ฑ
    0x0E26:5, // ฦ
    // group 6
    0x0E08:6, // จ
    0x0E25:6, // ล
    0x0E27:6, // ว
    0x0E2D:6, // อ
    0x0E2C:6, // ฬ
    // group 7
    0x0E28:7, // ศ
    0x0E2A:7, // ส
    0x0E0B:7, // ซ
    // group 8
    0x0E22:8, // ย
    // 0x0E1E:8, // พ - conflict, let's use 5 from doc "ย พ ฟ ผ ฝ"
    0x0E1F:8, // ฟ
    0x0E1C:8, // ผ
    0x0E1D:8, // ฝ
    // group 9
    // ญ already 4, ฐ
    0x0E10:9, // ฐ
    // สระ
    0x0E32:1, // า
    0x0E38:1, // ุ
    0x0E33:1, // ำ
    0x0E40:2, // เ
    0x0E41:2, // แ
    0x0E39:2, // ู
    0x0E42:4, // โ
    0x0E30:4, // ะ
    0x0E34:4, // ิ
    0x0E31:4, // ั ไม้หันอากาศ
    0x0E35:5, // ี สระอี
    0x0E43:6, // ใ สระใ
    // 0x0E35:7, // ี สระอี / อือ - conflict
    0x0E37:7, // ึ
    0x0E36:7, // ึ? let me use correct
    0x0E44:9, // ไ สระไ
    // ไม้ไต่คู้
    0x0E47:8, // ็
    // วรรณยุกต์
    0x0E48:1, // ่ ไม้เอก
    0x0E49:2, // ้ ไม้โท
    0x0E4A:3, // ๊ ไม้จัตวา (doc says ไม้จัตวา=3)
    0x0E4B:7, // ๋ ไม้ตรี (doc says ไม้ตรี=7)
    // การันต์
    0x0E4C:9, // ์
  };

  // Fix conflicts: พ appears in both 5 and 8. Doc says "ย พ ฟ ผ ฝ" for group 8
  // So พ=8, override
  fullMap[0x0E1E] = 8; // พ -> 8

  // ี appears in 5 (สระอี) and 7... doc says group 7: ส ซ / สระ อี อือ
  // group 5: สระอี
  // Let's say ี = 5 as สระอี based on "5: ฉ ณ ฒ น ม ห ฮ ฤ พ / สระอี"
  // and group 7 says "ส ซ / สระ อี, อือ / ไม้ตรี"
  // This is ambiguous - use 5 for ี

  return (fullMap as Record<number, number>)[code] || null;
}

