export const vowelCodes = new Set([0x0E30,0x0E31,0x0E32,0x0E33,0x0E34,0x0E35,0x0E36,0x0E37,0x0E38,0x0E39,0x0E40,0x0E41,0x0E42,0x0E43,0x0E44,0x0E47,0x0E4D]);

export function isVowel(ch: string) {
  return vowelCodes.has(ch.charCodeAt(0));
}

export function calcName(name: string) {
  let sum = 0;
  const breakdown = [];
  for (const ch of name) {
    const v = getCharValue(ch);
    if (v) {
      sum += v;
      breakdown.push({ ch, v });
    }
  }
  return { sum, breakdown };
}

export function checkKalakinee(text: string, dayIndex: number | null) {
  if (dayIndex === null) return [];
  const k = (kalakineeMap as Record<number, { day: string; chars: string[] }>)[dayIndex];
  if (!k) return [];
  const bad = [];
  for (const ch of text) {
    if (dayIndex === 1) {
      const code = ch.charCodeAt(0);
      // สระทั้งหมด ยกเว้น ไม้หันอากาศ (ั U+0E31) และการันต์ (์ U+0E4E)
      if (isVowel(ch) && code !== 0x0E31 && code !== 0x0E4E) bad.push(ch);
    } else {
      if (k.chars.includes(ch)) bad.push(ch);
    }
  }
  return bad;
};



